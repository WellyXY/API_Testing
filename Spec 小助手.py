def forecast_metrics(df, prediction_days=90):
    """预测未来的用户和收入指标"""
    # 获取最后一天的数据作为起点
    last_date = df["date"].max()
    cutoff_date = last_date + timedelta(days=1)
    
    historical_data = df.copy()
    
    # 准备cohort数据
    cohort_new_users = historical_data[["date", "new_users"]].copy()
    cohort_new_paying = historical_data[["date", "new_paying_users"]].copy()
    
    # 打印一些调试信息
    print(f"历史数据范围: {historical_data['date'].min()} 到 {historical_data['date'].max()}")
    print(f"预测起始日期: {cutoff_date}")
    print(f"历史新用户数据点数量: {len(cohort_new_users)}")
    print(f"最近的新用户数据:")
    print(cohort_new_users.tail())
    
    # 计算历史ARPPU
    historical_arppu = historical_data["daily_arppu"].mean()
    if pd.isna(historical_arppu):
        historical_arppu = 30.0
    
    # 获取留存率数据
    new_user_daily_retention = get_new_user_retention()
    print(f"留存率数据点数量: {len(new_user_daily_retention)}")
    
    # 预测未来数据
    prediction_start = cutoff_date
    prediction_dates = [prediction_start + timedelta(days=i) for i in range(prediction_days)]
    
    forecast_result = []
    for future_date in prediction_dates:
        # 预测DAU
        daily_active_users = 0
        for idx, row in cohort_new_users.iterrows():
            joined_date = row["date"]
            new_user_count = row["new_users"]
            
            days_diff = (future_date - joined_date).days
            if days_diff < 0 or days_diff >= len(new_user_daily_retention):
                continue
            retention_rate = new_user_daily_retention[days_diff]
            daily_active_users += new_user_count * retention_rate
        
        # 预测付费用户
        daily_paying_users = 0
        for idx, row in cohort_new_paying.iterrows():
            paid_date = row["date"]
            new_paying_count = row["new_paying_users"]
            
            days_diff = (future_date - paid_date).days
            if days_diff < 0:
                continue
            pay_ret = get_paying_retention_coefficient(days_diff)
            daily_paying_users += new_paying_count * pay_ret
        
        # 预测收入
        daily_revenue = daily_paying_users * historical_arppu
        
        forecast_result.append({
            "date": future_date,
            "forecast_DAU": daily_active_users,
            "forecast_paying_users": daily_paying_users,
            "forecast_revenue": daily_revenue
        })
    
    return pd.DataFrame(forecast_result)

